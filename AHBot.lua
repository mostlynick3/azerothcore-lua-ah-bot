function bitAnd(a, b)
    local result = 0
    local shift = 0
    while a > 0 or b > 0 do
        -- Check the least significant bit of both numbers
        if a % 2 == 1 and b % 2 == 1 then
            result = result + 2^shift
        end
        -- Right shift both numbers by 1 (essentially dividing by 2)
        a = math.floor(a / 2)
        b = math.floor(b / 2)
        shift = shift + 1
    end
    return result
end

